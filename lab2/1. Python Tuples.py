#1 indexing
fruits = ("apple", "banana", "cherry")
print(fruits[0])
#2 Number of items
fruits = ("apple", "banana", "cherry")
print(len(fruits))
#3 Negative indexing 
fruits = ("apple", "banana", "cherry")
print(fruits[-1])
#4 Range of indexes
fruits = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(fruits[2:5])